package variable.ex;

public class VarEx1Question {
    public static void main(String[] args) {
        int num1 = 4;
        int num2 = 3;

        System.out.println(num1 + num2);

        int num3 = 10;
        int num4 = 20;

        int sum = num3 + num4;
        System.out.println(sum);

        long l = 100000000000L;
        System.out.println(l);

        boolean b =  true;
        System.out.println(b);
    }
}
